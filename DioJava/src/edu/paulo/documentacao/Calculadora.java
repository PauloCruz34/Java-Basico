public class Calculadora {

	/**
	 * Este método é utilizado para somar dois numeros inteiros
	 * 
	 * @param numeroUm   primeiro parametro do método
	 * @param numeroDois segundo parametro do método
	 * @return int resultado deste métodp é a soma de dois números
	 */
	public int somar(int numeroUm, int numeroDois) {
		return numeroUm + numeroDois;
	}
}
